document.addEventListener('DOMContentLoaded', function () {
    const params = new URLSearchParams(window.location.search);
    const newsId = parseInt(params.get("id"));

    // Дефолтный язык
    let currentLanguage = localStorage.getItem('selectedLanguage') || 'ru';

    // Загружаем языковой файл
    function loadLanguage(lang) {
        fetch(`/lang/${lang}.json`)
            .then(response => response.json())
            .then(translations => {
                updatePageContent(translations, lang);
                localStorage.setItem('selectedLanguage', lang);
            })
            .catch(error => console.error("Ошибка загрузки языка:", error));
    }

    function updatePageContent(translations, lang) {
        const currentNews = newsData.find((news) => news.id === newsId);

        if (currentNews) {
            // Обновляем заголовок
            document.querySelector(".news-title").textContent = currentNews.title[lang];

            // Обновляем изображение
            document.querySelector(".news-image").src = currentNews.image[lang];

            // Обновляем текст новости
            document.querySelector(".news-content").innerHTML = currentNews.content[lang];
        } else {
            // Если новость не найдена
            document.querySelector(".news-page").innerHTML = `
                <div class="news-container">
                    <h1 class="news-title" style="cursor: auto">${translations.notFound}</h1>
                    <img src="/images/news_images/error.png" alt="Изображение новости" class="news-image">
                    <div class="news-content">
                        <p>${translations.descriptionNotFound}</p>
                    </div>
                </div>
            `;
        }
    }

    // Обработчик смены языка
    document.querySelectorAll('.circle').forEach(circle => {
        circle.addEventListener('click', function () {
            const selectedLang = this.getAttribute('data-lang');
            loadLanguage(selectedLang);
        });
    });

    // Загружаем начальный язык
    loadLanguage(currentLanguage);
});